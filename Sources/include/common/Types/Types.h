#ifndef TYPES_H
#define TYPES_H
/*! \brief Simplifie le cast en int */
#define Int(a) (int)(a)
/*! \brief Simplifie le cast en float */
#define Float(a) (float)(a)
/*! \brief Simplifie le cast en double */
#define Double(a) (double)(a)
/*! \brief Simplifie le cast en long int */
#define LInt(a) (long int)(a)
/*! \brief Simplifie le cast en void* */
#define Ptr(a) (void*)(a)
#endif
